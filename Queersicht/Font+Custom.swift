import SwiftUI

extension Font {
    static func globerBlack(size: CGFloat) -> Font {
        return .custom("Glober Black", size: size)
    }

    static func favoritStdRegular(size: CGFloat) -> Font {
        return .custom("favorit-std-regular", size: size)
    }
}
